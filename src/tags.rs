use std::hash::BuildHasherDefault;

use anyhow::{anyhow, Ok, Result};
use indexmap::IndexSet;
use phf::{phf_map, phf_ordered_set, Map, OrderedSet};
use rustc_hash::FxHasher;

type FxBuildHasher = BuildHasherDefault<FxHasher>;
type FxIndexSet<T> = IndexSet<T, FxBuildHasher>;
type TagSet = FxIndexSet<&'static [u8]>;

pub(crate) struct Tags {
    set: TagSet,
    last_index: usize,
}

impl Default for Tags {
    fn default() -> Self {
        let mut set = TagSet::default();
        for &tag in TAGS.iter() {
            set.insert(tag);
        }
        let last_index = set.len() - 1;
        Self { set, last_index }
    }
}

impl Tags {
    pub(crate) fn from(arg_tags: &[String]) -> Result<Self> {
        let mut set = TagSet::default();
        for &tag in TAGS.iter() {
            if arg_tags.iter().any(|arg_tag| arg_tag.as_bytes() == tag) {
                set.insert(tag);
            }
        }
        let last_index = set
            .len()
            .checked_sub(1)
            .ok_or_else(|| anyhow!("no valid tags"))?;
        Ok(Self { set, last_index })
    }
    pub(crate) fn get_index(&self, tag: &[u8]) -> Option<usize> {
        self.set.get_index_of(tag)
    }
    // It is the caller's responsibility to provide an in-bounds index. This is
    // ensured in csv.rs.
    pub(crate) fn index(&self, i: usize) -> &'static [u8] {
        self.set.get_index(i).expect("index in bounds")
    }
    pub(crate) fn last_index(&self) -> usize {
        self.last_index
    }
    pub(crate) fn len(&self) -> usize {
        self.set.len()
    }
}

